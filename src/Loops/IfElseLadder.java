package Loops;

public class IfElseLadder {
    public static void main(String[] args) {

        int marks=75;
        if(marks==40)
        {
            System.out.println("Candidate passed");
        }
        else if (marks==60) {
            System.out.println("Candidate passed in first class");

        } else if (marks==75) {
            System.out.println("Candidate passed with distinction");

        } else  {
            System.out.println("Candidate fail wow congratulation");

        }
    }
}
